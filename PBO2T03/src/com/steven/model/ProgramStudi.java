package com.steven.model;

public class ProgramStudi {
    private String kode;
    private String nama;
    private Fakultas faluktas;

    public String getKode() {
        return kode;
    }

    public void setKode(String kode) {
        this.kode = kode;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public Fakultas getFaluktas() {
        return faluktas;
    }

    public void setFaluktas(Fakultas faluktas) {
        this.faluktas = faluktas;
    }
}
